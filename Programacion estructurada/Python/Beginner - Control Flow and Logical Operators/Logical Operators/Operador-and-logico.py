# Ejemplo: Verificando si un número está dentro de un rango
# El operador 'and' asegura que ambas condiciones sean True.

numero = 25
# Verificamos si el número está entre 10 y 30 (inclusive)
if 10 <= numero <= 30:
    print("El número está dentro del rango.")
else:
    print("El número está fuera del rango.")